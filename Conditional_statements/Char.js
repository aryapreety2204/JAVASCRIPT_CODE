let char=prompt("enter any character:");
if (char>='A' && char<='Z'){
  console.log("the given character is upper case:");
}
else if(char>='a' && char<='z'){
  console.log("the given character is  lower case:");
}
else if(char>=0 && char<=9){
  console.log("it's a numeric number:")
}
else{
  console.log("the given character is special symbol:");
}





